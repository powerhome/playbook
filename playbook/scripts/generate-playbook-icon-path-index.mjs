import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const iconsRoot = path.resolve(projectRoot, "../node_modules/@powerhome/playbook-icons/icons");
const outputPath = path.resolve(
  projectRoot,
  "app/pb_kits/playbook/utilities/icons/generated/playbookIconPathIndex.ts",
);

if (!fs.existsSync(iconsRoot)) {
  throw new Error(`Could not find Playbook icons at ${iconsRoot}`);
}

const iconPathIndex = {};

for (const category of fs.readdirSync(iconsRoot).sort()) {
  const categoryPath = path.join(iconsRoot, category);
  if (!fs.statSync(categoryPath).isDirectory()) continue;

  for (const fileName of fs.readdirSync(categoryPath).sort()) {
    if (!fileName.endsWith(".svg")) continue;

    const iconName = fileName.replace(/\.svg$/, "");
    iconPathIndex[iconName] = `${category}/${fileName}`;
  }
}

const fileContents = `// This file is generated by scripts/generate-playbook-icon-path-index.mjs
// It maps Playbook icon names to their relative asset paths so React can
// request the same raw SVG files Rails resolves on the server.

const playbookIconPathIndex: Record<string, string> = ${JSON.stringify(iconPathIndex, null, 2)} as const;

export default playbookIconPathIndex;
`;

fs.writeFileSync(outputPath, fileContents);
